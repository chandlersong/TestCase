package com.hilatest.googledata.exception;

public class GoogleServiceDownException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 5984621335920040837L;

	public GoogleServiceDownException() {
		super();
	}

	public GoogleServiceDownException(String message, Throwable cause) {
		super(message, cause);
	}

	public GoogleServiceDownException(String message) {
		super(message);
	}

	public GoogleServiceDownException(Throwable cause) {
		super(cause);
	}
	
	

}
